/**
 * This interface specifies two behaviors, turn-on and turn-off,
 * that are common to many devices.
 *
 * @author author name
 * @version  1.0.0
 */
public interface  Device {

	/**
	 * Turns the device off.
	 */
	void turnOff();

	/**
	 * Turns the device on.
	 */
	void turnOn();
}
